#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*arr;
	int		i;

	i = 0;
	while (s1[i] != '\0')
		i++;
	arr = (char *)malloc(sizeof(char) * (i + 1));
	if (!arr)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		arr[i] = s1[i];
		i++;
	}
	arr[i] = '\0';
	return (arr);
}
